import { expect } from 'chai';
import { ethers } from 'hardhat';
import { describeFilter } from '@solidstate/library';
import { describeBehaviorOfERC721Base } from './ERC721Base.behavior';
import { describeBehaviorOfERC721Enumerable } from './ERC721Enumerable.behavior';
import { describeBehaviorOfERC721Metadata } from './ERC721Metadata.behavior';
import { ERC721 } from '../../../typechain';
import { SignerWithAddress } from '@nomiclabs/hardhat-ethers/signers';
import { BigNumber, ContractTransaction } from 'ethers';

interface ERC721BehaviorArgs {
  deploy: () => Promise<ERC721>;
  mint: (address: string, tokenId: BigNumber) => Promise<ContractTransaction>;
  burn: (tokenId: BigNumber) => Promise<ContractTransaction>;
  supply: BigNumber,
  name: string;
  symbol: string;
  tokenURI: string;
}

export function describeBehaviorOfERC721(
  { deploy, supply, mint, burn, name, symbol, tokenURI }: ERC721BehaviorArgs,
  skips?: string[],
) {
  const describe = describeFilter(skips);

  describe('::ERC721', function () {
    let holder: SignerWithAddress;

    let instance: ERC721;

    before(async function () {
      [holder] = await ethers.getSigners();
    })

    beforeEach(async function () {
      instance = await deploy();
    });

    describeBehaviorOfERC721Base(
      {
        deploy,
        supply,
        mint,
        burn,
      },
      skips,
    );

    describeBehaviorOfERC721Enumerable(
      {
        deploy,
        supply,
        mint,
        burn,
      },
      ['::ERC721Base', ...(skips ?? [])],
    );

    describeBehaviorOfERC721Metadata(
      {
        deploy,
        name,
        symbol,
        tokenURI,
      },
      skips,
    );

    describe('#transferFrom', function () {
      describe('reverts if', function () {
        it('value is included in transaction', async function () {
          const tokenId = ethers.constants.Two;
          await mint(holder.address, tokenId);

          await expect(
            instance.connect(holder).transferFrom(
              holder.address,
              holder.address,
              tokenId,
              { value: ethers.constants.One }
            )
          ).to.be.revertedWith(
            'ERC721: payable transfer calls not supported'
          );
        })
      });
    });

    describe('#safeTransferFrom', function () {
      describe('(address,address,uint256)', function () {
        describe('reverts if', function () {
          it('value is included in transaction', async function () {
            const tokenId = ethers.constants.Two;
            await mint(holder.address, tokenId);

            await expect(
              instance.connect(holder)['safeTransferFrom(address,address,uint256)'](
                holder.address,
                holder.address,
                tokenId,
                { value: ethers.constants.One }
              )
            ).to.be.revertedWith(
              'ERC721: payable transfer calls not supported'
            );
          });
        });
      });

      describe('(address,address,uint256,bytes)', function () {
        describe('reverts if', function () {
          it('value is included in transaction', async function () {
            const tokenId = ethers.constants.Two;
            await mint(holder.address, tokenId);

            await expect(
              instance.connect(holder)['safeTransferFrom(address,address,uint256,bytes)'](
                holder.address,
                holder.address,
                tokenId,
                '0x',
                { value: ethers.constants.One }
              )
            ).to.be.revertedWith(
              'ERC721: payable transfer calls not supported'
            );
          });
        });
      });
    });

    describe('#approve', function () {
      describe('reverts if', function () {
        it('value is included in transaction', async function () {
          const tokenId = ethers.constants.Two;
          await mint(holder.address, tokenId);

          await expect(
            instance.connect(holder).approve(
              ethers.constants.AddressZero,
              tokenId,
              { value: ethers.constants.One }
            )
          ).to.be.revertedWith(
            'ERC721: payable approve calls not supported'
          );
        })
      });
    });
  });
}
